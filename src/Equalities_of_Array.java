import java.util.Arrays;

public class Equalities_of_Array {

    //Program to test the equalities of two arrays
    //main method
    public static void main(String[]args){
        //creating element
        //  m1 is array 1
        int[] m1 = new int[]{11,12,13,14,15};
        //m2 is array 2
        int[] m2 = new int[]{11,12,13,14,15};
        //m3 is array 3
        int[] m3 = new int[]{14,16,17,15,18};

        System.out.println("m1 equals to m2 :" + Arrays.equals(m1,m2)); //true
        System.out.println("is m1 equals to m3 : " + Arrays.equals(m1,m3)); //False








    }










}
